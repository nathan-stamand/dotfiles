-- Requires tmux-sessionizer (install via cargo)
-- Configure a search path for tms and this opens a menu of your projects
-- within that path, which you can fuzzy find in the search and create a new
-- tmux session on select. It's incredibly hype
vim.keymap.set("n", "<C-f>", "<cmd>silent !tmux neww tms<CR>")
vim.keymap.set("n", "<leader>f", vim.lsp.buf.format)

